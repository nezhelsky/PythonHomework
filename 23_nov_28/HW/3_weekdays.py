days = ["Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье"]

i = 0
while i < 7:
    print("День недели -", days[i])
    user_input = input("Хотите увидеть следующий день? 1-да, 0-нет: ")
    if user_input == "0":
        print("Вывод дней недели прерван пользователем. Конец")
        break
    elif user_input == "1":
        i += 1
    if i == 7:
        i = 0